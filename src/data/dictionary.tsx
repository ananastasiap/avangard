export function getDictionaryEntryText(node: ReactNode): string {
  if (node === null || node === undefined || typeof node === "boolean") {
    return "";
  }

  if (typeof node === "string" || typeof node === "number") {
    return String(node);
  }

  if (Array.isArray(node)) {
    return node.map(getDictionaryEntryText).join(" ");
  }

  if (isValidElement<{ children?: ReactNode }>(node)) {
    return getDictionaryEntryText(node.props.children);
  }

  return "";
}
